public class TuDienEntry {
    private String tu;
    private String yNghia;

    public TuDienEntry(String tu, String yNghia) {
        this.tu = tu;
        this.yNghia = yNghia;
    }

    public String getTu() {
        return tu;
    }

    public String getyNghia() {
        return yNghia;
    }
}
